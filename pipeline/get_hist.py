import sys
file_in = open(sys.argv[1],'r')
file_out = open(sys.argv[2],'w')

hist={}

for line in file_in:
	line = line.rstrip()
	fields = line.split()
#	print fields
	hist[int(fields[1])] = hist.get(int(fields[1]), 0) + 1
	
maxk = max(hist.keys())

for i in range(maxk + 1):
	file_out.write('%d %d\n' % (i, hist.get(i, 0)))

